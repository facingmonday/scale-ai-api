const mongoose = require("mongoose");
const { queues, ensureQueueReady } = require("./index");

const Scenario = require("../../services/scenario/scenario.model");
const ScenarioOutcome = require("../../services/scenarioOutcome/scenarioOutcome.model");
const JobService = require("../../services/job/lib/jobService");

const {
  autoCreateSubmissionsForScenario,
} = require("../../services/submission/autoCreateSubmissionsForScenario");
const {
  forwardPreviousSubmissionsForScenario,
} = require("../../services/submission/forwardPreviousSubmissionsForScenario");
const {
  useDefaultsForSubmissions,
} = require("../../services/submission/useDefaultsForSubmissions");

const {
  enqueueSimulationBatchSubmit,
} = require("./simulation-batch-worker");

async function ensureDbConnected() {
  if (mongoose.connection.readyState === 1) return;
  await mongoose.connect(process.env.MONGO_URL || process.env.MONGO_URI);
}

/**
 * Outcome processing worker:
 * - Optionally auto-generate missing submissions (USE_AI / USE_DEFAULTS / FORWARD_PREVIOUS)
 * - Create SimulationJobs for all submissions with controlled concurrency
 * - If batch mode, enqueue a single scenario-level batch submit job
 * - Close scenario at the end (so submission creation remains valid)
 */
const processOutcomeProcessingJob = async (job) => {
  const { scenarioId, organizationId, clerkUserId } = job.data || {};
  if (!scenarioId || !organizationId || !clerkUserId) {
    throw new Error(
      "Missing required fields in outcome processing payload (scenarioId, organizationId, clerkUserId)"
    );
  }

  await ensureDbConnected();

  const scenario = await Scenario.findOne({
    _id: scenarioId,
    organization: organizationId,
  });
  if (!scenario) {
    throw new Error("Scenario not found");
  }
  if (scenario.isClosed) {
    return { success: true, skipped: true, reason: "Scenario already closed" };
  }

  const outcome = await ScenarioOutcome.getOutcomeByScenario(scenarioId);
  if (!outcome) {
    throw new Error("Scenario outcome not found");
  }

  // Auto-generate submissions based on outcome settings
  const autoGenerateMode = outcome.autoGenerateSubmissionsOnOutcome;
  let autoGenerateResult = null;

  if (autoGenerateMode === "USE_AI") {
    autoGenerateResult = await autoCreateSubmissionsForScenario({
      scenarioId,
      organizationId,
      clerkUserId,
      options: {
        includeExisting: true,
        concurrency: Number(process.env.AUTO_SUBMISSION_CONCURRENCY || 10),
      },
      punishAbsentStudents: outcome.punishAbsentStudents,
    });
  } else if (autoGenerateMode === "USE_DEFAULTS") {
    autoGenerateResult = await useDefaultsForSubmissions({
      scenarioId,
      organizationId,
      clerkUserId,
    });
  } else if (autoGenerateMode === "FORWARD_PREVIOUS") {
    autoGenerateResult = await forwardPreviousSubmissionsForScenario({
      scenarioId,
      organizationId,
      clerkUserId,
      punishAbsentStudents: outcome.punishAbsentStudents,
    });
  }

  // Create jobs for all submissions (dryRun = false, will write to ledger)
  const simulationMode = String(process.env.SIMULATION_MODE || "direct");
  const useBatch = simulationMode === "batch";

  const jobs = await JobService.createJobsForScenario(
    scenarioId,
    scenario.classroomId,
    false,
    organizationId,
    clerkUserId,
    {
      enqueue: !useBatch,
    }
  );

  if (useBatch) {
    // Batch mode: mark scenario as processing; do NOT close yet.
    // Scenario will close when batch completes (handled in simulation-batch-worker).
    await scenario.setBatchProcessingStatus("processing", clerkUserId);
    await enqueueSimulationBatchSubmit({
      scenarioId,
      classroomId: scenario.classroomId,
      organizationId,
      clerkUserId,
    });
  } else {
    // Direct mode: close scenario immediately (jobs run synchronously in worker).
    await scenario.close(clerkUserId);
  }

  return {
    success: true,
    scenarioId,
    jobsCreated: jobs.length,
    autoGeneratedSubmissions: autoGenerateResult
      ? {
          created: autoGenerateResult.created || 0,
          existing: autoGenerateResult.existing || 0,
          missingStore: autoGenerateResult.missingStore,
          missingPrevious: autoGenerateResult.missingPrevious,
          errors: autoGenerateResult.errors?.length || 0,
        }
      : null,
    batchEnqueued: useBatch,
  };
};

const initOutcomeProcessingWorker = () => {
  console.log("🧰 Initializing outcome processing worker...");

  const parsedConcurrency = parseInt(
    process.env.OUTCOME_PROCESSING_CONCURRENCY || "1",
    10
  );
  const concurrency = Number.isFinite(parsedConcurrency)
    ? Math.max(1, parsedConcurrency)
    : 1;

  queues.outcomeProcessing.process(
    "process-outcome",
    concurrency,
    processOutcomeProcessingJob
  );

  queues.outcomeProcessing.on("completed", (job, result) => {
    console.log(
      `✅ Outcome processing completed: ${job.data?.scenarioId || job.id}`,
      result && typeof result === "object" ? result : undefined
    );
  });

  queues.outcomeProcessing.on("failed", (job, err) => {
    const id = job?.data?.scenarioId || job?.id || "unknown";
    console.error(`❌ Outcome processing failed: ${id} - ${err.message}`);
  });

  console.log(
    `✅ Outcome processing worker initialized (concurrency: ${concurrency})`
  );
};

/**
 * Enqueue an outcome processing job.
 * Uses a deterministic jobId to avoid duplicate enqueues per scenario.
 */
const enqueueOutcomeProcessing = async ({
  scenarioId,
  organizationId,
  clerkUserId,
}) => {
  await ensureQueueReady(queues.outcomeProcessing, "outcomeProcessing");

  return queues.outcomeProcessing.add(
    "process-outcome",
    { scenarioId, organizationId, clerkUserId },
    {
      jobId: `scenario-outcome:${String(scenarioId)}`,
      removeOnComplete: true,
      removeOnFail: false,
      attempts: 3,
      backoff: { type: "exponential", delay: 1000 },
    }
  );
};

module.exports = {
  initOutcomeProcessingWorker,
  enqueueOutcomeProcessing,
  processOutcomeProcessingJob,
};

